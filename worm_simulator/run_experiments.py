#!/usr/bin/env python
"""
Batch Ablation Experiment Runner
=================================

Runs multiple ablation conditions systematically for comparative analysis.

Usage:
    python run_experiments.py

This will:
    1. Define your experimental conditions
    2. Modify ablation_config.py for each
    3. Run simulation
    4. Save metrics with condition label
    5. Generate comparison plots
"""

import os
import shutil
import subprocess
import sys
import pandas as pd
import matplotlib.pyplot as plt
import numpy as np
from pathlib import Path

# Define experimental conditions
EXPERIMENTS = {
    "01_normal": {
        "ENABLE_PHEROMONE": True,
        "ENABLE_EVOLUTION": True,
        "ENABLE_NEURAL_BIAS": True,
        "ENABLE_CLUSTERING": True,
        "description": "Full system (baseline)"
    },
    "02_no_pheromone": {
        "ENABLE_PHEROMONE": False,
        "ENABLE_EVOLUTION": True,
        "ENABLE_NEURAL_BIAS": True,
        "ENABLE_CLUSTERING": True,
        "description": "Worms cannot smell pheromones"
    },
    "03_no_evolution": {
        "ENABLE_PHEROMONE": True,
        "ENABLE_EVOLUTION": False,
        "ENABLE_NEURAL_BIAS": True,
        "ENABLE_CLUSTERING": True,
        "description": "Genetics don't change (static population)"
    },
    "04_neural_only": {
        "ENABLE_PHEROMONE": False,
        "ENABLE_EVOLUTION": False,
        "ENABLE_NEURAL_BIAS": True,
        "ENABLE_CLUSTERING": False,
        "description": "Only neural circuits (isolated individuals)"
    },
}

ABLATION_CONFIG_TEMPLATE = '''"""
Ablation Configuration for Research Experiments
================================================

AUTO-GENERATED BY: run_experiments.py
Current Experiment: {experiment_name}
Description: {description}

DO NOT EDIT - Use run_experiments.py to modify
"""

ENABLE_PHEROMONE = {enable_pheromone}
ENABLE_EVOLUTION = {enable_evolution}
ENABLE_NEURAL_BIAS = {enable_neural_bias}
ENABLE_CLUSTERING = {enable_clustering}

SAVE_METRICS = True
VERBOSE_ABLATIONS = False

def get_ablation_summary():
    """Return human-readable ablation status."""
    return f"""
╔════════════════════════════════════════╗
║  EXPERIMENT: {experiment_name_padded}║
║  {description_padded}║
╠════════════════════════════════════════╣
║ Pheromone Sensing: {{str(ENABLE_PHEROMONE).ljust(21)}}║
║ Evolution:         {{str(ENABLE_EVOLUTION).ljust(21)}}║
║ Neural Bias:       {{str(ENABLE_NEURAL_BIAS).ljust(21)}}║
║ Clustering:        {{str(ENABLE_CLUSTERING).ljust(21)}}║
║ Save Metrics:      {{str(SAVE_METRICS).ljust(21)}}║
╚════════════════════════════════════════╝
    """
'''


def modify_ablation_config(experiment_name, config_dict):
    """Update ablation_config.py with experiment settings."""
    desc = config_dict.get("description", "")
    name_pad = experiment_name.ljust(23)
    desc_pad = desc.ljust(32)
    
    content = ABLATION_CONFIG_TEMPLATE.format(
        experiment_name=experiment_name,
        experiment_name_padded=name_pad,
        description_padded=desc_pad,
        enable_pheromone=config_dict["ENABLE_PHEROMONE"],
        enable_evolution=config_dict["ENABLE_EVOLUTION"],
        enable_neural_bias=config_dict["ENABLE_NEURAL_BIAS"],
        enable_clustering=config_dict["ENABLE_CLUSTERING"],
    )
    
    with open("ablation_config.py", "w") as f:
        f.write(content)


def run_experiment(experiment_name, run_number, total_runs):
    """Execute single experiment run."""
    print(f"\n{'='*60}")
    print(f"  EXPERIMENT: {experiment_name}")
    print(f"  RUN {run_number}/{total_runs}")
    print(f"{'='*60}")
    
    try:
        result = subprocess.run(
            [sys.executable, "main_gpu.py"],
            timeout=300,  # 5 minute timeout
            capture_output=False
        )
        return result.returncode == 0
    except subprocess.TimeoutExpired:
        print(f"⚠️  Experiment timeout (5 min)")
        return False
    except KeyboardInterrupt:
        print(f"\n⏸️  Experiment interrupted by user")
        raise
    except Exception as e:
        print(f"❌ Error running experiment: {e}")
        return False


def save_metrics_with_label(experiment_name, run_number):
    """Move metrics.csv to labeled file."""
    if not os.path.exists("metrics.csv"):
        print(f"⚠️  No metrics.csv found")
        return False
    
    output_filename = f"results/metrics_{experiment_name}_run_{run_number}.csv"
    os.makedirs("results", exist_ok=True)
    
    try:
        shutil.copy("metrics.csv", output_filename)
        print(f"✓ Saved to {output_filename}")
        return True
    except Exception as e:
        print(f"❌ Error saving metrics: {e}")
        return False


def generate_comparison_plots():
    """Create comparison plots from all experiment runs."""
    results_dir = "results"
    if not os.path.exists(results_dir):
        print("❌ No results directory found")
        return
    
    # Load all metrics files
    experiment_data = {}
    for filepath in Path(results_dir).glob("metrics_*.csv"):
        # Extract experiment name from filename
        parts = filepath.stem.replace("metrics_", "").rsplit("_run_", 1)
        if len(parts) != 2:
            continue
        exp_name, run_num = parts
        
        if exp_name not in experiment_data:
            experiment_data[exp_name] = []
        
        df = pd.read_csv(filepath)
        experiment_data[exp_name].append(df)
    
    if not experiment_data:
        print("❌ No experiment data found")
        return
    
    # Aggregate statistics
    stats = {}
    for exp_name, dfs in experiment_data.items():
        if not dfs:
            continue
        
        # Get final values from each run
        final_msd = [df["msd"].iloc[-1] if len(df) > 0 else 0 for df in dfs]
        final_cluster = [df["max_cluster_size"].iloc[-1] if len(df) > 0 else 0 for df in dfs]
        final_pop = [df["population"].iloc[-1] if len(df) > 0 else 0 for df in dfs]
        
        stats[exp_name] = {
            "msd_mean": np.mean(final_msd),
            "msd_std": np.std(final_msd),
            "cluster_mean": np.mean(final_cluster),
            "cluster_std": np.std(final_cluster),
            "pop_mean": np.mean(final_pop),
            "pop_std": np.std(final_pop),
        }
    
    # Create comparison plot
    fig, axes = plt.subplots(1, 3, figsize=(15, 5))
    fig.suptitle("Ablation Experiment Comparison", fontsize=14, fontweight='bold')
    
    exp_names = sorted(stats.keys())
    
    # Plot 1: MSD
    ax = axes[0]
    means = [stats[e]["msd_mean"] for e in exp_names]
    stds = [stats[e]["msd_std"] for e in exp_names]
    ax.bar(range(len(exp_names)), means, yerr=stds, capsize=5, alpha=0.7, color='blue')
    ax.set_xticks(range(len(exp_names)))
    ax.set_xticklabels(exp_names, rotation=45, ha='right')
    ax.set_ylabel("Mean Squared Displacement")
    ax.set_title("Movement Distance")
    ax.grid(True, alpha=0.3, axis='y')
    
    # Plot 2: Clustering
    ax = axes[1]
    means = [stats[e]["cluster_mean"] for e in exp_names]
    stds = [stats[e]["cluster_std"] for e in exp_names]
    ax.bar(range(len(exp_names)), means, yerr=stds, capsize=5, alpha=0.7, color='red')
    ax.set_xticks(range(len(exp_names)))
    ax.set_xticklabels(exp_names, rotation=45, ha='right')
    ax.set_ylabel("Max Cluster Size")
    ax.set_title("Clustering Behavior")
    ax.grid(True, alpha=0.3, axis='y')
    
    # Plot 3: Population
    ax = axes[2]
    means = [stats[e]["pop_mean"] for e in exp_names]
    stds = [stats[e]["pop_std"] for e in exp_names]
    ax.bar(range(len(exp_names)), means, yerr=stds, capsize=5, alpha=0.7, color='green')
    ax.set_xticks(range(len(exp_names)))
    ax.set_xticklabels(exp_names, rotation=45, ha='right')
    ax.set_ylabel("Final Population")
    ax.set_title("Population Size")
    ax.grid(True, alpha=0.3, axis='y')
    
    plt.tight_layout()
    output_path = "results/comparison_ablations.png"
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    print(f"✓ Saved comparison plot to {output_path}")
    
    # Print summary table
    print("\n" + "="*70)
    print("ABLATION EXPERIMENT SUMMARY")
    print("="*70)
    print(f"{'Condition':<25} {'MSD':<15} {'Cluster':<15} {'Pop':<15}")
    print("-"*70)
    for exp_name in exp_names:
        msd = stats[exp_name]["msd_mean"]
        cluster = stats[exp_name]["cluster_mean"]
        pop = stats[exp_name]["pop_mean"]
        print(f"{exp_name:<25} {msd:<15.1f} {cluster:<15.1f} {pop:<15.0f}")
    print("="*70)


def main():
    """Run full experiment suite."""
    print("\n🧬 WORM SIMULATOR: ABLATION EXPERIMENT RUNNER\n")
    
    total_experiments = len(EXPERIMENTS)
    print(f"Found {total_experiments} experimental conditions")
    
    run_count = 1
    for exp_name, config in EXPERIMENTS.items():
        print(f"\n[{run_count}/{total_experiments}] Setting up {exp_name}")
        print(f"    {config['description']}")
        
        # Modify config
        modify_ablation_config(exp_name, config)
        
        # Run experiment
        success = run_experiment(exp_name, 1, 1)
        
        if success:
            # Save results
            save_metrics_with_label(exp_name, 1)
        else:
            print(f"⚠️  Experiment failed, skipping metrics save")
        
        run_count += 1
    
    # Generate comparison plots
    print("\n📊 Generating comparison plots...")
    generate_comparison_plots()
    
    print("\n✅ All experiments complete!")
    print("📁 Results saved to results/")


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\n\n🛑 Experiment run cancelled by user")
        sys.exit(1)
